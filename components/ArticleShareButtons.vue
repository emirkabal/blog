  methods: {
    copy(text: string) {
      this.copied = true
      setTimeout(() => {
        this.hide = true
      }, 500)
      const temp = document.createElement("textarea")
      temp.textContent = text
      temp.setAttribute("style", "position:absolute; right: 200%;")
